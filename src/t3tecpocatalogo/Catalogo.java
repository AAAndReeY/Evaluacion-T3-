public class Catalogo {
    
    private String NombreRopas;
    private String PrecioRopas;
    private String CodRopa;

    public String getNombreRopas() {
        return NombreRopas;
    }

    public void setNombreRopas(String NombreRopas) {
        this.NombreRopas = NombreRopas;
    }

    public String getPrecioRopas() {
        return PrecioRopas;
    }

    public void setPrecioRopas(String PrecioRopas) {
        this.PrecioRopas = PrecioRopas;
    }

    public String getCodRopa() {
        return CodRopa;
    }

    public void setCodRopa(String CodRopa) {
        this.CodRopa = CodRopa;
    }
    
   
    
    
}
